#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*src2;

	i = 0;
	src2 = (char *)src;
	while (src2[i])
	{
		dst[i] = src2[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
