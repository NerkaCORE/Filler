#include "libft.h"

int		ft_ishex(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	if (c >= 'A' && c <= 'F')
		return (1);
	if (c >= 'a' && c <= 'f')
		return (1);
	if (c == 'x' || c == 'X')
		return (1);
	return (0);
}
